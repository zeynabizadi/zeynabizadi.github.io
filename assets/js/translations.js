// تابع راه‌اندازی Typed.js
function initTyped() {
  const typedElement = document.querySelector('.typed');
  if (typedElement) {
    const typedItems = typedElement.getAttribute('data-typed-items');
    if (typedItems) {
      const strings = typedItems.split(',').map(item => item.trim());
      
      if (window.typed) {
        window.typed.destroy();
      }
      
      window.typed = new Typed('.typed', {
        strings: strings,
        typeSpeed: 100,
        backSpeed: 50,
        backDelay: 2000,
        loop: true
      });
    }
  }
}

// تابع راه‌اندازی اولیه
function initLanguage() {
  // دریافت زبان ذخیره شده یا استفاده از زبان پیش‌فرض
  const savedLang = localStorage.getItem('selectedLanguage') || 'en';
  changeLanguage(savedLang);
  
  // راه‌اندازی Typed.js
  initTyped();
}

// اضافه کردن event listener برای دکمه‌های تغییر زبان
document.addEventListener('DOMContentLoaded', function() {
  // اضافه کردن event listener برای دکمه‌های زبان
  document.querySelectorAll('.language-switcher a').forEach(btn => {
    btn.addEventListener('click', function(e) {
      e.preventDefault();
      const lang = this.getAttribute('data-lang');
      changeLanguage(lang);
    });
  });
  
  // راه‌اندازی اولیه
  initLanguage();
});
